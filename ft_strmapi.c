#include <libft.h>

char	*ft_strmapi(char const *s, char (*f)(size_t, char))
{
	char	*d;
	size_t	i;

	d = ft_strdup(s);
	i = -1;
	while (d[++i])
		d[i] = f(i, d[i]);
	return (d);
}
